
import { serve } from "https://deno.land/std@0.168.0/http/server.ts"
import { createClient } from 'https://esm.sh/@supabase/supabase-js@2'

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
}

interface FixRequest {
  email: string
  corrections?: Array<{
    type: 'missing_user' | 'wrong_password' | 'unconfirmed_email'
    action: string
  }>
  checkOnly?: boolean
}

interface FixResult {
  email: string
  userExists?: boolean
  userData?: any
  corrections?: Array<{
    action: string
    status: 'success' | 'failed'
    message: string
    timestamp: string
  }>
  success: boolean
  totalCorrections?: number
  successfulCorrections?: number
  warnings?: string[]
  loginValidated?: boolean
  clientMessage?: string
}

serve(async (req) => {
  // Handle CORS preflight requests
  if (req.method === 'OPTIONS') {
    return new Response('ok', { headers: corsHeaders })
  }

  try {
    console.log('üîß [FixClientAuth] === OPERA√á√ÉO INICIADA ===')

    const { email, corrections, checkOnly }: FixRequest = await req.json()
    
    if (!email) {
      throw new Error('Email √© obrigat√≥rio')
    }

    // Criar cliente Supabase com service_role para privil√©gios administrativos
    const supabaseUrl = Deno.env.get('SUPABASE_URL')!
    const supabaseServiceKey = Deno.env.get('SUPABASE_SERVICE_ROLE_KEY')!
    
    const supabaseAdmin = createClient(supabaseUrl, supabaseServiceKey, {
      auth: {
        autoRefreshToken: false,
        persistSession: false
      }
    })

    const normalizedEmail = email.toLowerCase().trim()
    console.log('üìß [FixClientAuth] Processando email:', normalizedEmail)

    // 1. ETAPA CR√çTICA: Verificar usu√°rio usando getUserByEmail (mais eficiente e preciso)
    console.log('üîç [FixClientAuth] Verificando usu√°rio com getUserByEmail...')
    
    let existingUser = null
    let userExists = false
    
    try {
      // CORRE√á√ÉO PRINCIPAL: Usar getUserByEmail ao inv√©s de listUsers + find
      const { data: userResponse, error: userError } = await supabaseAdmin.auth.admin.getUserByEmail(normalizedEmail)
      
      if (userError) {
        console.log('‚ö†Ô∏è [FixClientAuth] getUserByEmail retornou erro:', userError.message)
        // Fallback para listUsers caso getUserByEmail falhe
        console.log('üîÑ [FixClientAuth] Tentando fallback com listUsers...')
        const { data: usersResponse, error: usersError } = await supabaseAdmin.auth.admin.listUsers()
        
        if (usersError) {
          console.error('‚ùå [FixClientAuth] Fallback tamb√©m falhou:', usersError)
          throw new Error(`Erro ao verificar usu√°rio: ${usersError.message}`)
        }
        
        existingUser = usersResponse.users.find(u => u.email?.toLowerCase() === normalizedEmail)
      } else {
        existingUser = userResponse.user
      }
      
      userExists = !!existingUser
      
      console.log('üîç [FixClientAuth] Resultado da verifica√ß√£o:', {
        userExists,
        userId: existingUser?.id,
        emailConfirmed: existingUser?.email_confirmed_at !== null
      })
      
    } catch (error) {
      console.error('‚ùå [FixClientAuth] Erro cr√≠tico na verifica√ß√£o:', error)
      throw error
    }

    // 2. Verificar cliente na base de dados (n√£o-bloqueante)
    let clienteExists = false
    let clienteData = null
    const warnings: string[] = []
    
    try {
      const { data: cliente, error: clienteError } = await supabaseAdmin
        .from('todos_clientes')
        .select('id, nome_cliente, email_cliente')
        .eq('email_cliente', normalizedEmail)
        .single()

      if (clienteError && clienteError.code !== 'PGRST116') {
        console.error('‚ö†Ô∏è [FixClientAuth] Erro ao buscar cliente:', clienteError)
        warnings.push(`Erro ao verificar cliente na base: ${clienteError.message}`)
      } else if (cliente) {
        clienteExists = true
        clienteData = cliente
        console.log('‚úÖ [FixClientAuth] Cliente encontrado:', cliente.nome_cliente)
      } else {
        console.log('‚ö†Ô∏è [FixClientAuth] Cliente n√£o encontrado na base de dados')
        warnings.push('Cliente n√£o encontrado na base de dados')
      }
    } catch (error) {
      console.error('‚ö†Ô∏è [FixClientAuth] Erro inesperado ao verificar cliente:', error)
      warnings.push(`Erro inesperado ao verificar cliente: ${error.message}`)
    }

    // Se for apenas verifica√ß√£o, retornar resultado
    if (checkOnly) {
      console.log('üîç [FixClientAuth] Modo verifica√ß√£o - retornando resultado')
      return new Response(
        JSON.stringify({
          email: normalizedEmail,
          userExists,
          userData: existingUser,
          success: true,
          warnings: warnings.length > 0 ? warnings : undefined
        }),
        {
          headers: { ...corsHeaders, 'Content-Type': 'application/json' },
          status: 200,
        },
      )
    }

    // 3. ETAPA CR√çTICA: Aplicar corre√ß√µes inteligentes se fornecidas
    if (!corrections || corrections.length === 0) {
      return new Response(
        JSON.stringify({
          email: normalizedEmail,
          userExists,
          userData: existingUser,
          success: true,
          warnings: warnings.length > 0 ? warnings : undefined
        }),
        {
          headers: { ...corsHeaders, 'Content-Type': 'application/json' },
          status: 200,
        },
      )
    }

    console.log('üîß [FixClientAuth] === APLICANDO CORRE√á√ïES INTELIGENTES ===')
    console.log('üìä [FixClientAuth] Total de corre√ß√µes:', corrections.length)

    const appliedCorrections: Array<{
      action: string
      status: 'success' | 'failed'
      message: string
      timestamp: string
    }> = []

    // APLICAR CADA CORRE√á√ÉO COM ROBUSTEZ M√ÅXIMA
    for (const correction of corrections) {
      console.log(`üîß [FixClientAuth] Aplicando: ${correction.type}`)
      
      try {
        switch (correction.type) {
          case 'missing_user':
            if (existingUser) {
              appliedCorrections.push({
                action: correction.action,
                status: 'failed',
                message: 'Usu√°rio j√° existe no sistema de autentica√ß√£o',
                timestamp: new Date().toISOString()
              })
              console.log('‚ö†Ô∏è [FixClientAuth] Usu√°rio j√° existe, pulando cria√ß√£o')
            } else {
              console.log('‚ûï [FixClientAuth] Criando usu√°rio...')
              const { data: newUser, error: createError } = await supabaseAdmin.auth.admin.createUser({
                email: normalizedEmail,
                password: 'parceriadesucesso',
                email_confirm: true,
                user_metadata: {
                  user_type: 'cliente',
                  created_by_fix_function: true,
                  created_at: new Date().toISOString(),
                  client_exists_in_db: clienteExists
                }
              })

              if (createError) {
                if (createError.message?.includes('already been registered')) {
                  // Se usu√°rio j√° existe, tentar resetar senha ao inv√©s de falhar
                  console.log('üîÑ [FixClientAuth] Usu√°rio j√° existe, convertendo para reset de senha...')
                  
                  // Buscar usu√°rio novamente
                  const { data: foundUser } = await supabaseAdmin.auth.admin.getUserByEmail(normalizedEmail)
                  if (foundUser?.user) {
                    const { error: updateError } = await supabaseAdmin.auth.admin.updateUserById(
                      foundUser.user.id,
                      { 
                        password: 'parceriadesucesso',
                        email_confirm: true
                      }
                    )

                    if (updateError) {
                      throw updateError
                    }

                    appliedCorrections.push({
                      action: 'Usu√°rio j√° existia - senha resetada',
                      status: 'success',
                      message: 'Usu√°rio j√° existia, senha resetada para "parceriadesucesso"',
                      timestamp: new Date().toISOString()
                    })
                    
                    existingUser = foundUser.user // Atualizar refer√™ncia
                    console.log('‚úÖ [FixClientAuth] Convers√£o bem-sucedida - senha resetada')
                  } else {
                    throw createError
                  }
                } else {
                  throw createError
                }
              } else {
                appliedCorrections.push({
                  action: correction.action,
                  status: 'success',
                  message: 'Usu√°rio criado com sucesso no sistema de autentica√ß√£o',
                  timestamp: new Date().toISOString()
                })
                
                console.log('‚úÖ [FixClientAuth] Usu√°rio criado:', newUser.user?.id)
                existingUser = newUser.user // Atualizar refer√™ncia
              }
            }
            break

          case 'wrong_password':
            if (!existingUser) {
              appliedCorrections.push({
                action: correction.action,
                status: 'failed',
                message: 'Usu√°rio n√£o existe para resetar senha - precisa ser criado primeiro',
                timestamp: new Date().toISOString()
              })
              console.log('‚ö†Ô∏è [FixClientAuth] Usu√°rio n√£o existe para resetar senha')
            } else {
              console.log('üîë [FixClientAuth] Resetando senha...')
              const { error: updateError } = await supabaseAdmin.auth.admin.updateUserById(
                existingUser.id,
                { 
                  password: 'parceriadesucesso',
                  email_confirm: true
                }
              )

              if (updateError) {
                throw updateError
              }

              appliedCorrections.push({
                action: correction.action,
                status: 'success',
                message: 'Senha resetada para "parceriadesucesso"',
                timestamp: new Date().toISOString()
              })
              
              console.log('‚úÖ [FixClientAuth] Senha resetada para usu√°rio:', existingUser.id)
            }
            break

          case 'unconfirmed_email':
            if (!existingUser) {
              appliedCorrections.push({
                action: correction.action,
                status: 'failed',
                message: 'Usu√°rio n√£o existe para confirmar email - precisa ser criado primeiro',
                timestamp: new Date().toISOString()
              })
              console.log('‚ö†Ô∏è [FixClientAuth] Usu√°rio n√£o existe para confirmar email')
            } else {
              console.log('‚úâÔ∏è [FixClientAuth] Confirmando email...')
              const { error: confirmError } = await supabaseAdmin.auth.admin.updateUserById(
                existingUser.id,
                { email_confirm: true }
              )

              if (confirmError) {
                throw confirmError
              }

              appliedCorrections.push({
                action: correction.action,
                status: 'success',
                message: 'Email confirmado automaticamente',
                timestamp: new Date().toISOString()
              })
              
              console.log('‚úÖ [FixClientAuth] Email confirmado para usu√°rio:', existingUser.id)
            }
            break

          default:
            appliedCorrections.push({
              action: correction.action,
              status: 'failed',
              message: 'Tipo de corre√ß√£o n√£o suportado',
              timestamp: new Date().toISOString()
            })
        }
      } catch (error: any) {
        console.error(`‚ùå [FixClientAuth] Erro na corre√ß√£o ${correction.type}:`, error)
        appliedCorrections.push({
          action: correction.action,
          status: 'failed',
          message: `Erro: ${error.message}`,
          timestamp: new Date().toISOString()
        })
      }
    }

    // 4. ETAPA CR√çTICA: Valida√ß√£o p√≥s-corre√ß√£o com login real
    let loginValidated = false
    console.log('üß™ [FixClientAuth] === VALIDA√á√ÉO P√ìS-CORRE√á√ÉO ===')
    
    const successfulCorrections = appliedCorrections.filter(c => c.status === 'success').length
    
    if (successfulCorrections > 0) {
      console.log('üîê [FixClientAuth] Testando login com senha padr√£o...')
      
      try {
        // Criar cliente normal para teste de login
        const supabaseTest = createClient(supabaseUrl, Deno.env.get('SUPABASE_ANON_KEY')!, {
          auth: {
            autoRefreshToken: false,
            persistSession: false
          }
        })

        const { data: loginData, error: loginError } = await supabaseTest.auth.signInWithPassword({
          email: normalizedEmail,
          password: 'parceriadesucesso'
        })

        if (!loginError && loginData.user) {
          loginValidated = true
          console.log('‚úÖ [FixClientAuth] Login validado com sucesso!')
          
          // Fazer logout imediato do teste
          await supabaseTest.auth.signOut()
        } else {
          console.log('‚ö†Ô∏è [FixClientAuth] Falha na valida√ß√£o do login:', loginError?.message)
        }
      } catch (error) {
        console.error('‚ùå [FixClientAuth] Erro no teste de login:', error)
      }
    }

    // 5. Log da opera√ß√£o no banco
    try {
      await supabaseAdmin
        .from('client_user_creation_log')
        .insert({
          email_cliente: normalizedEmail,
          operation_type: 'auto_corrections_v2',
          result_message: `Aplicadas ${successfulCorrections}/${appliedCorrections.length} corre√ß√µes. Login validado: ${loginValidated ? 'Sim' : 'N√£o'}. Cliente na base: ${clienteExists ? 'Sim' : 'N√£o'}`
        })
    } catch (logError) {
      console.error('‚ö†Ô∏è [FixClientAuth] Erro ao salvar log (n√£o cr√≠tico):', logError)
      warnings.push('Erro ao salvar log da opera√ß√£o')
    }

    // 6. ETAPA CR√çTICA: Gerar mensagem final pronta para o cliente
    const clientMessage = generateClientMessage(
      normalizedEmail, 
      clienteData?.nome_cliente, 
      appliedCorrections, 
      loginValidated, 
      warnings
    )

    const result: FixResult = {
      email: normalizedEmail,
      corrections: appliedCorrections,
      success: successfulCorrections > 0,
      totalCorrections: corrections.length,
      successfulCorrections,
      loginValidated,
      clientMessage,
      warnings: warnings.length > 0 ? warnings : undefined
    }

    console.log('üìù [FixClientAuth] === RESULTADO FINAL ===')
    console.log('‚úÖ Corre√ß√µes aplicadas:', successfulCorrections)
    console.log('üîê Login validado:', loginValidated)
    console.log('üì± Mensagem gerada para cliente:', clientMessage ? 'Sim' : 'N√£o')

    return new Response(
      JSON.stringify(result),
      {
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 200,
      },
    )

  } catch (error) {
    console.error('üí• [FixClientAuth] Erro fatal:', error)
    return new Response(
      JSON.stringify({
        success: false,
        error: error instanceof Error ? error.message : 'Erro desconhecido',
        corrections: [],
        totalCorrections: 0,
        successfulCorrections: 0,
        loginValidated: false
      }),
      {
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 200, // Sempre retornar 200 quando poss√≠vel
      },
    )
  }
})

function generateClientMessage(
  email: string, 
  nomeCliente: string | undefined, 
  corrections: Array<{action: string, status: string, message: string}>, 
  loginValidated: boolean,
  warnings?: string[]
): string {
  const successful = corrections.filter(c => c.status === 'success')
  const failed = corrections.filter(c => c.status === 'failed')
  
  let message = `‚úÖ ACESSO LIBERADO PARA O SISTEMA

Ol√° ${nomeCliente || 'Cliente'},

${loginValidated ? 'Seu acesso foi corrigido e est√° funcionando perfeitamente!' : 'Aplicamos as corre√ß√µes no seu acesso.'}`

  if (successful.length > 0) {
    message += `

üîß CORRE√á√ïES REALIZADAS:
${successful.map(c => `‚Ä¢ ${c.message}`).join('\n')}`
  }

  if (failed.length > 0) {
    message += `

‚ö†Ô∏è AVISOS:
${failed.map(c => `‚Ä¢ ${c.message}`).join('\n')}`
  }

  if (warnings && warnings.length > 0) {
    message += `

‚ÑπÔ∏è INFORMA√á√ïES T√âCNICAS:
${warnings.map(w => `‚Ä¢ ${w}`).join('\n')}`
  }

  message += `

üîë SUAS CREDENCIAIS DE ACESSO:
‚Ä¢ Email: ${email}
‚Ä¢ Senha: parceriadesucesso

üöÄ COMO ACESSAR O SISTEMA:
1. Acesse: https://login.trafegoporcents.com
2. Clique em "Entrar"
3. Digite seu email e senha exatamente como mostrado acima
4. Clique em "Entrar"

${loginValidated ? '‚úÖ STATUS: Acesso validado e funcionando' : '‚ö†Ô∏è STATUS: Aguardando teste de acesso'}
‚è∞ Processado em: ${new Date().toLocaleString('pt-BR')}

${loginValidated ? 'Seu acesso est√° 100% funcionando! Se tiver qualquer d√∫vida, estamos aqui.' : 'Teste o acesso e nos informe se houver algum problema.'}

Atenciosamente,
Equipe Suporte T√©cnico`

  return message
}
