
import { serve } from "https://deno.land/std@0.168.0/http/server.ts"
import { createClient } from 'https://esm.sh/@supabase/supabase-js@2'

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
}

interface FixRequest {
  email: string
  corrections?: Array<{
    type: 'missing_user' | 'wrong_password' | 'unconfirmed_email'
    action: string
  }>
  checkOnly?: boolean
}

interface FixResult {
  email: string
  userExists?: boolean
  userData?: any
  corrections?: Array<{
    action: string
    status: 'success' | 'failed'
    message: string
    timestamp: string
  }>
  success: boolean
  totalCorrections?: number
  successfulCorrections?: number
  warnings?: string[]
  loginValidated?: boolean
  clientMessage?: string
}

serve(async (req) => {
  // Handle CORS preflight requests
  if (req.method === 'OPTIONS') {
    return new Response('ok', { headers: corsHeaders })
  }

  try {
    console.log('üîß [FixClientAuth] === OPERA√á√ÉO INICIADA V4 ===')

    const { email, corrections, checkOnly }: FixRequest = await req.json()
    
    if (!email) {
      throw new Error('Email √© obrigat√≥rio')
    }

    // Criar cliente Supabase com service_role para privil√©gios administrativos
    const supabaseUrl = Deno.env.get('SUPABASE_URL')!
    const supabaseServiceKey = Deno.env.get('SUPABASE_SERVICE_ROLE_KEY')!
    
    const supabaseAdmin = createClient(supabaseUrl, supabaseServiceKey, {
      auth: {
        autoRefreshToken: false,
        persistSession: false
      }
    })

    const normalizedEmail = email.toLowerCase().trim()
    console.log('üìß [FixClientAuth] Processando email:', normalizedEmail)

    // 1. DETEC√á√ÉO MELHORADA: Usar getUserByEmail primeiro, depois listUsers como fallback
    console.log('üîç [FixClientAuth] === DETEC√á√ÉO DUPLA DE USU√ÅRIO ===')
    
    let existingUser = null
    let userExists = false
    let detectionMethod = ''
    
    try {
      // M√âTODO 1: getUserByEmail (mais confi√°vel para usu√°rios existentes)
      console.log('üîç [FixClientAuth] Tentando getUserByEmail...')
      
      const { data: userByEmail, error: getUserError } = await supabaseAdmin.auth.admin.getUserByEmail(normalizedEmail)
      
      if (!getUserError && userByEmail.user) {
        existingUser = userByEmail.user
        userExists = true
        detectionMethod = 'getUserByEmail'
        console.log('‚úÖ [FixClientAuth] Usu√°rio encontrado via getUserByEmail:', existingUser.id)
      } else {
        console.log('‚ö†Ô∏è [FixClientAuth] getUserByEmail n√£o encontrou:', getUserError?.message)
        
        // M√âTODO 2: listUsers como fallback
        console.log('üîç [FixClientAuth] Fallback para listUsers...')
        
        const { data: usersResponse, error: usersError } = await supabaseAdmin.auth.admin.listUsers()
        
        if (usersError) {
          console.error('‚ùå [FixClientAuth] Erro ao listar usu√°rios:', usersError)
          throw new Error(`Erro ao verificar usu√°rio: ${usersError.message}`)
        }
        
        existingUser = usersResponse.users.find(u => u.email?.toLowerCase() === normalizedEmail)
        userExists = !!existingUser
        detectionMethod = 'listUsers'
        
        if (userExists) {
          console.log('‚úÖ [FixClientAuth] Usu√°rio encontrado via listUsers:', existingUser.id)
        } else {
          console.log('‚ùå [FixClientAuth] Usu√°rio n√£o encontrado em nenhum m√©todo')
        }
      }
      
    } catch (error) {
      console.error('‚ùå [FixClientAuth] Erro cr√≠tico na verifica√ß√£o:', error)
      throw error
    }

    console.log(`üîç [FixClientAuth] Resultado da detec√ß√£o: ${userExists ? 'EXISTE' : 'N√ÉO EXISTE'} (m√©todo: ${detectionMethod})`)

    // 2. Verificar cliente na base de dados (n√£o-bloqueante)
    let clienteExists = false
    let clienteData = null
    const warnings: string[] = []
    
    try {
      const { data: clientes, error: clienteError } = await supabaseAdmin
        .from('todos_clientes')
        .select('id, nome_cliente, email_cliente')
        .eq('email_cliente', normalizedEmail)

      if (clienteError) {
        console.error('‚ö†Ô∏è [FixClientAuth] Erro ao buscar cliente:', clienteError)
        warnings.push(`Erro ao verificar cliente na base: ${clienteError.message}`)
      } else if (clientes && clientes.length > 0) {
        clienteExists = true
        clienteData = clientes[0]
        console.log('‚úÖ [FixClientAuth] Cliente encontrado:', clienteData.nome_cliente)
        
        if (clientes.length > 1) {
          warnings.push(`Encontrados ${clientes.length} registros duplicados - usando o primeiro`)
          console.log(`‚ö†Ô∏è [FixClientAuth] ${clientes.length} registros duplicados encontrados`)
        }
      } else {
        console.log('‚ö†Ô∏è [FixClientAuth] Cliente n√£o encontrado na base de dados')
        warnings.push('Cliente n√£o encontrado na base de dados')
      }
    } catch (error) {
      console.error('‚ö†Ô∏è [FixClientAuth] Erro inesperado ao verificar cliente:', error)
      warnings.push(`Erro inesperado ao verificar cliente: ${error.message}`)
    }

    // Se for apenas verifica√ß√£o, retornar resultado
    if (checkOnly) {
      console.log('üîç [FixClientAuth] Modo verifica√ß√£o - retornando resultado')
      return new Response(
        JSON.stringify({
          email: normalizedEmail,
          userExists,
          userData: existingUser,
          success: true,
          warnings: warnings.length > 0 ? warnings : undefined
        }),
        {
          headers: { ...corsHeaders, 'Content-Type': 'application/json' },
          status: 200,
        },
      )
    }

    // 3. L√ìGICA INTELIGENTE: Priorizar reset de senha para resolver conflitos
    console.log('üîß [FixClientAuth] === APLICANDO CORRE√á√ïES INTELIGENTES V4 ===')

    const appliedCorrections: Array<{
      action: string
      status: 'success' | 'failed'
      message: string
      timestamp: string
    }> = []

    let correctionSuccessful = false

    // ESTRAT√âGIA INTELIGENTE: Se h√° algum ind√≠cio de que o usu√°rio existe, tentar reset primeiro
    if (userExists || detectionMethod === 'getUserByEmail') {
      console.log('üîë [FixClientAuth] Usu√°rio existe - aplicando reset de senha...')
      
      try {
        const { error: updateError } = await supabaseAdmin.auth.admin.updateUserById(
          existingUser.id,
          { 
            password: 'parceriadesucesso',
            email_confirm: true
          }
        )

        if (updateError) {
          throw updateError
        }

        appliedCorrections.push({
          action: 'Reset de senha e confirma√ß√£o de email',
          status: 'success',
          message: 'Senha resetada para "parceriadesucesso" e email confirmado',
          timestamp: new Date().toISOString()
        })
        
        correctionSuccessful = true
        console.log('‚úÖ [FixClientAuth] Reset de senha aplicado com sucesso')
        
      } catch (error: any) {
        console.error('‚ùå [FixClientAuth] Erro no reset de senha:', error)
        appliedCorrections.push({
          action: 'Reset de senha',
          status: 'failed',
          message: `Erro no reset: ${error.message}`,
          timestamp: new Date().toISOString()
        })
      }
    } else {
      // CRIA√á√ÉO DE USU√ÅRIO como segunda op√ß√£o
      console.log('‚ûï [FixClientAuth] Usu√°rio n√£o existe - criando novo usu√°rio...')
      
      try {
        const { data: newUser, error: createError } = await supabaseAdmin.auth.admin.createUser({
          email: normalizedEmail,
          password: 'parceriadesucesso',
          email_confirm: true,
          user_metadata: {
            user_type: 'cliente',
            created_by_fix_function: true,
            created_at: new Date().toISOString(),
            client_exists_in_db: clienteExists,
            nome_cliente: clienteData?.nome_cliente || 'Cliente'
          }
        })

        if (createError) {
          if (createError.message?.includes('already been registered')) {
            console.log('üîÑ [FixClientAuth] Convers√£o: usu√°rio j√° existe, tentando reset...')
            
            // FALLBACK AUTOM√ÅTICO: Buscar usu√°rio novamente e resetar senha
            const { data: usersRetry } = await supabaseAdmin.auth.admin.listUsers()
            const foundUser = usersRetry.users.find(u => u.email?.toLowerCase() === normalizedEmail)
            
            if (foundUser) {
              const { error: updateError } = await supabaseAdmin.auth.admin.updateUserById(
                foundUser.id,
                { 
                  password: 'parceriadesucesso',
                  email_confirm: true
                }
              )

              if (updateError) {
                throw updateError
              }

              appliedCorrections.push({
                action: 'Criar usu√°rio (convertido automaticamente para reset)',
                status: 'success',
                message: 'Usu√°rio j√° existia - senha resetada automaticamente',
                timestamp: new Date().toISOString()
              })
              
              existingUser = foundUser
              correctionSuccessful = true
              console.log('‚úÖ [FixClientAuth] Fallback autom√°tico bem-sucedido')
            } else {
              throw createError
            }
          } else {
            throw createError
          }
        } else {
          appliedCorrections.push({
            action: 'Criar usu√°rio no sistema de autentica√ß√£o',
            status: 'success',
            message: 'Usu√°rio criado com sucesso',
            timestamp: new Date().toISOString()
          })
          
          console.log('‚úÖ [FixClientAuth] Usu√°rio criado:', newUser.user?.id)
          existingUser = newUser.user
          correctionSuccessful = true
        }
      } catch (error: any) {
        console.error('‚ùå [FixClientAuth] Erro na cria√ß√£o/fallback:', error)
        appliedCorrections.push({
          action: 'Criar usu√°rio no sistema de autentica√ß√£o',
          status: 'failed',
          message: `Erro: ${error.message}`,
          timestamp: new Date().toISOString()
        })
      }
    }

    // 4. VALIDA√á√ÉO REAL P√ìS-CORRE√á√ÉO com login de teste
    let loginValidated = false
    console.log('üß™ [FixClientAuth] === VALIDA√á√ÉO P√ìS-CORRE√á√ÉO ===')
    
    if (correctionSuccessful) {
      console.log('üîê [FixClientAuth] Testando login com credenciais...')
      
      try {
        // Criar cliente normal para teste de login
        const supabaseTest = createClient(supabaseUrl, Deno.env.get('SUPABASE_ANON_KEY')!, {
          auth: {
            autoRefreshToken: false,
            persistSession: false
          }
        })

        const { data: loginData, error: loginError } = await supabaseTest.auth.signInWithPassword({
          email: normalizedEmail,
          password: 'parceriadesucesso'
        })

        if (!loginError && loginData.user) {
          loginValidated = true
          console.log('‚úÖ [FixClientAuth] Login validado com sucesso!')
          
          // Fazer logout imediato do teste
          await supabaseTest.auth.signOut()
          
          // Atualizar o status da corre√ß√£o para refletir o sucesso completo
          if (appliedCorrections.length > 0) {
            appliedCorrections[appliedCorrections.length - 1].message += ' - Login validado'
          }
        } else {
          console.log('‚ö†Ô∏è [FixClientAuth] Falha na valida√ß√£o do login:', loginError?.message)
          warnings.push(`Login de teste falhou: ${loginError?.message}`)
        }
      } catch (error) {
        console.error('‚ùå [FixClientAuth] Erro no teste de login:', error)
        warnings.push(`Erro no teste de login: ${error.message}`)
      }
    }

    // 5. Log da opera√ß√£o no banco
    try {
      await supabaseAdmin
        .from('client_user_creation_log')
        .insert({
          email_cliente: normalizedEmail,
          operation_type: 'intelligent_corrections_v4',
          result_message: `M√©todo detec√ß√£o: ${detectionMethod}. Corre√ß√£o aplicada: ${correctionSuccessful ? 'Sim' : 'N√£o'}. Login validado: ${loginValidated ? 'Sim' : 'N√£o'}. Cliente na base: ${clienteExists ? 'Sim' : 'N√£o'}. Duplicatas: ${warnings.some(w => w.includes('duplicados')) ? 'Sim' : 'N√£o'}`
        })
    } catch (logError) {
      console.error('‚ö†Ô∏è [FixClientAuth] Erro ao salvar log (n√£o cr√≠tico):', logError)
      warnings.push('Erro ao salvar log da opera√ß√£o')
    }

    // 6. Gerar mensagem final otimizada
    const clientMessage = generateClientMessage(
      normalizedEmail, 
      clienteData?.nome_cliente, 
      appliedCorrections, 
      loginValidated, 
      warnings
    )

    const successfulCorrections = appliedCorrections.filter(c => c.status === 'success').length
    
    const result: FixResult = {
      email: normalizedEmail,
      corrections: appliedCorrections,
      success: correctionSuccessful,
      totalCorrections: Math.max(appliedCorrections.length, 1),
      successfulCorrections,
      loginValidated,
      clientMessage,
      warnings: warnings.length > 0 ? warnings : undefined
    }

    console.log('üìù [FixClientAuth] === RESULTADO FINAL V4 ===')
    console.log('‚úÖ Corre√ß√µes aplicadas:', successfulCorrections)
    console.log('üîê Login validado:', loginValidated)
    console.log('üìä Sucesso geral:', correctionSuccessful)
    console.log('üéØ M√©todo de detec√ß√£o usado:', detectionMethod)

    return new Response(
      JSON.stringify(result),
      {
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 200,
      },
    )

  } catch (error) {
    console.error('üí• [FixClientAuth] Erro fatal:', error)
    return new Response(
      JSON.stringify({
        success: false,
        error: error instanceof Error ? error.message : 'Erro desconhecido',
        corrections: [],
        totalCorrections: 0,
        successfulCorrections: 0,
        loginValidated: false
      }),
      {
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 200,
      },
    )
  }
})

