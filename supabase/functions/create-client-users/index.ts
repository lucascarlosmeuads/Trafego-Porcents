
import { serve } from "https://deno.land/std@0.168.0/http/server.ts"
import { createClient } from 'https://esm.sh/@supabase/supabase-js@2'

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
}

interface ProcessResult {
  email: string
  operation: 'created' | 'updated' | 'skipped' | 'error'
  message: string
}

serve(async (req) => {
  // Handle CORS preflight requests
  if (req.method === 'OPTIONS') {
    return new Response('ok', { headers: corsHeaders })
  }

  try {
    console.log('üöÄ [CreateClientUsers] Iniciando processamento de usu√°rios clientes')

    // Parse request body to check for specific client email
    let requestBody: any = {}
    try {
      const text = await req.text()
      if (text) {
        requestBody = JSON.parse(text)
      }
    } catch (error) {
      console.log('üìù [CreateClientUsers] Sem body na requisi√ß√£o ou body inv√°lido, processando todos os clientes')
    }

    // Criar cliente Supabase com service_role para privil√©gios administrativos
    const supabaseUrl = Deno.env.get('SUPABASE_URL')!
    const supabaseServiceKey = Deno.env.get('SUPABASE_SERVICE_ROLE_KEY')!
    
    const supabaseAdmin = createClient(supabaseUrl, supabaseServiceKey, {
      auth: {
        autoRefreshToken: false,
        persistSession: false
      }
    })

    const SENHA_PADRAO = 'parceriadesucesso'
    const results: ProcessResult[] = []

    // Verificar se foi solicitado processamento de cliente espec√≠fico
    if (requestBody.clientEmail) {
      console.log(`üéØ [CreateClientUsers] Processamento espec√≠fico para: ${requestBody.clientEmail}`)
      
      const clientEmail = requestBody.clientEmail.trim().toLowerCase()
      
      // Validar formato de e-mail b√°sico
      const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/
      if (!emailRegex.test(clientEmail)) {
        return new Response(
          JSON.stringify({
            success: false,
            error: 'Email inv√°lido fornecido'
          }),
          {
            headers: { ...corsHeaders, 'Content-Type': 'application/json' },
            status: 400,
          },
        )
      }

      // Excluir e-mails do sistema
      if (clientEmail.includes('@trafegoporcents.com') || clientEmail.startsWith('admin@')) {
        return new Response(
          JSON.stringify({
            success: true,
            message: 'Email do sistema - n√£o processado',
            statistics: { total: 0, created: 0, updated: 0, skipped: 1, errors: 0 },
            results: [{
              email: clientEmail,
              operation: 'skipped',
              message: 'E-mail pertence ao sistema - n√£o processado'
            }]
          }),
          {
            headers: { ...corsHeaders, 'Content-Type': 'application/json' },
            status: 200,
          },
        )
      }

      // Verificar se usu√°rio j√° existe
      const { data: existingUsers, error: usersError } = await supabaseAdmin.auth.admin.listUsers()
      
      if (usersError) {
        console.error('‚ùå [CreateClientUsers] Erro ao buscar usu√°rios existentes:', usersError)
        throw usersError
      }

      const existingUser = existingUsers.users.find(u => u.email?.toLowerCase() === clientEmail)
      let result: ProcessResult

      if (existingUser) {
        // Verificar se √© um gestor/admin
        const { data: gestorCheck } = await supabaseAdmin
          .from('gestores')
          .select('email')
          .eq('email', clientEmail)
          .single()

        if (gestorCheck) {
          result = {
            email: clientEmail,
            operation: 'skipped',
            message: 'E-mail pertence a um gestor - n√£o alterado'
          }
          console.log(`‚è≠Ô∏è [CreateClientUsers] ${clientEmail} - Gestor detectado, pulando`)
        } else {
          // √â um cliente - atualizar senha
          const { error: updateError } = await supabaseAdmin.auth.admin.updateUserById(
            existingUser.id,
            { password: SENHA_PADRAO }
          )

          if (updateError) {
            result = {
              email: clientEmail,
              operation: 'error',
              message: `Erro ao atualizar senha: ${updateError.message}`
            }
            console.error(`‚ùå [CreateClientUsers] ${clientEmail} - Erro ao atualizar:`, updateError)
          } else {
            result = {
              email: clientEmail,
              operation: 'updated',
              message: 'Senha atualizada para padr√£o do sistema'
            }
            console.log(`‚úÖ [CreateClientUsers] ${clientEmail} - Senha atualizada`)
          }
        }
      } else {
        // Usu√°rio n√£o existe - criar novo
        const { data: newUser, error: createError } = await supabaseAdmin.auth.admin.createUser({
          email: clientEmail,
          password: SENHA_PADRAO,
          email_confirm: true, // Confirmar e-mail automaticamente
          user_metadata: {
            user_type: 'cliente',
            created_by_system: true,
            created_at: new Date().toISOString()
          }
        })

        if (createError) {
          result = {
            email: clientEmail,
            operation: 'error',
            message: `Erro ao criar usu√°rio: ${createError.message}`
          }
          console.error(`‚ùå [CreateClientUsers] ${clientEmail} - Erro ao criar:`, createError)
        } else {
          result = {
            email: clientEmail,
            operation: 'created',
            message: 'Usu√°rio criado com sucesso'
          }
          console.log(`‚úÖ [CreateClientUsers] ${clientEmail} - Usu√°rio criado`)
        }
      }

      results.push(result)

      // Log da opera√ß√£o no banco (sem aguardar)
      supabaseAdmin
        .from('client_user_creation_log')
        .insert({
          email_cliente: clientEmail,
          operation_type: result.operation,
          result_message: result.message
        })
        .then(() => console.log(`üìù [CreateClientUsers] Log registrado para ${clientEmail}`))
        .catch(error => console.error(`‚ùå [CreateClientUsers] Erro ao registrar log:`, error))

      // Estat√≠sticas finais
      const stats = {
        total: 1,
        created: result.operation === 'created' ? 1 : 0,
        updated: result.operation === 'updated' ? 1 : 0,
        skipped: result.operation === 'skipped' ? 1 : 0,
        errors: result.operation === 'error' ? 1 : 0
      }

      console.log(`üéØ [CreateClientUsers] Processamento espec√≠fico conclu√≠do:`, stats)

      return new Response(
        JSON.stringify({
          success: true,
          message: `Processamento de cliente espec√≠fico conclu√≠do: ${result.operation}`,
          statistics: stats,
          results: results
        }),
        {
          headers: { ...corsHeaders, 'Content-Type': 'application/json' },
          status: 200,
        },
      )
    }

    // Processamento em lote (comportamento original)
    console.log('üìä [CreateClientUsers] Iniciando processamento em lote de todos os clientes')

    // Buscar todos os e-mails √∫nicos da tabela todos_clientes
    const { data: clientes, error: clientesError } = await supabaseAdmin
      .from('todos_clientes')
      .select('email_cliente')
      .not('email_cliente', 'is', null)
      .neq('email_cliente', '')

    if (clientesError) {
      console.error('‚ùå [CreateClientUsers] Erro ao buscar clientes:', clientesError)
      throw clientesError
    }

    console.log(`üìä [CreateClientUsers] Encontrados ${clientes?.length || 0} registros de clientes`)

    // Extrair e-mails √∫nicos e v√°lidos
    const emailsUnicos = [...new Set(
      clientes
        ?.map(c => c.email_cliente?.trim().toLowerCase())
        .filter(email => {
          if (!email) return false
          
          // Validar formato de e-mail b√°sico
          const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/
          if (!emailRegex.test(email)) return false
          
          // Excluir e-mails do sistema
          if (email.includes('@trafegoporcents.com')) return false
          if (email.startsWith('admin@')) return false
          
          return true
        }) || []
    )]

    console.log(`‚úÖ [CreateClientUsers] ${emailsUnicos.length} e-mails √∫nicos e v√°lidos para processar`)

    // Buscar todos os usu√°rios existentes para verifica√ß√£o
    const { data: existingUsers, error: usersError } = await supabaseAdmin.auth.admin.listUsers()
    
    if (usersError) {
      console.error('‚ùå [CreateClientUsers] Erro ao buscar usu√°rios existentes:', usersError)
      throw usersError
    }

    const existingUserEmails = new Set(existingUsers.users.map(u => u.email?.toLowerCase()))
    console.log(`üìã [CreateClientUsers] ${existingUsers.users.length} usu√°rios j√° existem no sistema`)

    // Processar cada e-mail
    for (const email of emailsUnicos) {
      try {
        console.log(`üîç [CreateClientUsers] Processando: ${email}`)
        
        let result: ProcessResult

        if (existingUserEmails.has(email)) {
          // Usu√°rio j√° existe - verificar se √© cliente e atualizar senha
          const existingUser = existingUsers.users.find(u => u.email?.toLowerCase() === email)
          
          if (existingUser) {
            // Verificar se √© um gestor/admin (baseado no e-mail)
            const { data: gestorCheck } = await supabaseAdmin
              .from('gestores')
              .select('email')
              .eq('email', email)
              .single()

            if (gestorCheck) {
              result = {
                email,
                operation: 'skipped',
                message: 'E-mail pertence a um gestor - n√£o alterado'
              }
              console.log(`‚è≠Ô∏è [CreateClientUsers] ${email} - Gestor detectado, pulando`)
            } else {
              // √â um cliente - atualizar senha
              const { error: updateError } = await supabaseAdmin.auth.admin.updateUserById(
                existingUser.id,
                { password: SENHA_PADRAO }
              )

              if (updateError) {
                result = {
                  email,
                  operation: 'error',
                  message: `Erro ao atualizar senha: ${updateError.message}`
                }
                console.error(`‚ùå [CreateClientUsers] ${email} - Erro ao atualizar:`, updateError)
              } else {
                result = {
                  email,
                  operation: 'updated',
                  message: 'Senha atualizada para padr√£o do sistema'
                }
                console.log(`‚úÖ [CreateClientUsers] ${email} - Senha atualizada`)
              }
            }
          } else {
            result = {
              email,
              operation: 'error',
              message: 'Usu√°rio encontrado mas n√£o foi poss√≠vel acessar dados'
            }
          }
        } else {
          // Usu√°rio n√£o existe - criar novo
          const { data: newUser, error: createError } = await supabaseAdmin.auth.admin.createUser({
            email: email,
            password: SENHA_PADRAO,
            email_confirm: true, // Confirmar e-mail automaticamente
            user_metadata: {
              user_type: 'cliente',
              created_by_system: true,
              created_at: new Date().toISOString()
            }
          })

          if (createError) {
            result = {
              email,
              operation: 'error',
              message: `Erro ao criar usu√°rio: ${createError.message}`
            }
            console.error(`‚ùå [CreateClientUsers] ${email} - Erro ao criar:`, createError)
          } else {
            result = {
              email,
              operation: 'created',
              message: 'Usu√°rio criado com sucesso'
            }
            console.log(`‚úÖ [CreateClientUsers] ${email} - Usu√°rio criado`)
          }
        }

        results.push(result)

        // Log da opera√ß√£o no banco
        await supabaseAdmin
          .from('client_user_creation_log')
          .insert({
            email_cliente: email,
            operation_type: result.operation,
            result_message: result.message
          })

        // Rate limiting - pausa entre opera√ß√µes apenas no processamento em lote
        await new Promise(resolve => setTimeout(resolve, 100))

      } catch (error) {
        console.error(`üí• [CreateClientUsers] Erro inesperado para ${email}:`, error)
        const errorResult: ProcessResult = {
          email,
          operation: 'error',
          message: `Erro inesperado: ${error instanceof Error ? error.message : 'Erro desconhecido'}`
        }
        results.push(errorResult)

        // Log do erro no banco
        await supabaseAdmin
          .from('client_user_creation_log')
          .insert({
            email_cliente: email,
            operation_type: 'error',
            result_message: errorResult.message
          })
      }
    }

    // Estat√≠sticas finais
    const stats = {
      total: results.length,
      created: results.filter(r => r.operation === 'created').length,
      updated: results.filter(r => r.operation === 'updated').length,
      skipped: results.filter(r => r.operation === 'skipped').length,
      errors: results.filter(r => r.operation === 'error').length
    }

    console.log(`üìà [CreateClientUsers] Processamento em lote conclu√≠do:`, stats)

    return new Response(
      JSON.stringify({
        success: true,
        message: 'Processamento de usu√°rios clientes conclu√≠do',
        statistics: stats,
        results: results
      }),
      {
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 200,
      },
    )

  } catch (error) {
    console.error('üí• [CreateClientUsers] Erro fatal:', error)
    return new Response(
      JSON.stringify({
        success: false,
        error: error instanceof Error ? error.message : 'Erro desconhecido'
      }),
      {
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 500,
      },
    )
  }
})
